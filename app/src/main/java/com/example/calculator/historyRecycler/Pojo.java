package com.example.calculator.historyRecycler;

public class Pojo {
    String questions;
    String answers;

    public Pojo(String questions, String answers) {
        this.questions = questions;
        this.answers = answers;
    }

    public String getQuestions() {
        return questions;
    }

    public String getAnswers() {
        return answers;
    }

}
